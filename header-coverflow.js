function isEmbedVideoSrc(src) {
  return isCloudinaryEmbedVideoSrc(src) || isMegaShareVideoSrc(src);
}

function isCloudinaryEmbedVideoSrc(src) {
  return /^https?:\/\/player\.cloudinary\.com\/embed\//i.test(src);
}

function isMegaShareVideoSrc(src) {
  return /^https?:\/\/mega\.nz\/file\//i.test(src);
}

function getCloudinaryDirectVideoSrc(src, mode = "card") {
  if (!isCloudinaryEmbedVideoSrc(src)) {
    return src;
  }

  try {
    const url = new URL(src);
    const cloudName = url.searchParams.get("cloud_name");
    const publicId = url.searchParams.get("public_id");

    if (!cloudName || !publicId) {
      return src;
    }

    const normalizedPublicId = decodeURIComponent(publicId)
      .split("/")
      .map((segment) => encodeURIComponent(segment))
      .join("/");

    const transform = mode === "lightbox"
      ? "f_auto,q_auto:best,vc_auto"
      : "f_auto,q_auto:good,vc_auto,w_960";

    return `https://res.cloudinary.com/${encodeURIComponent(cloudName)}/video/upload/${transform}/${normalizedPublicId}.mp4`;
  } catch (_error) {
    return src;
  }
}

function getCloudinaryPosterSrc(src) {
  try {
    if (/^https?:\/\/res\.cloudinary\.com\//i.test(src) && /\/video\/upload\//i.test(src)) {
      return src
        .replace(/\/video\/upload\//i, "/video/upload/f_auto,q_auto:good,so_0/")
        .replace(/\.mp4(\?.*)?$/i, ".jpg");
    }

    if (!isCloudinaryEmbedVideoSrc(src)) {
      return "";
    }

    const url = new URL(src);
    const cloudName = url.searchParams.get("cloud_name");
    const publicId = url.searchParams.get("public_id");

    if (!cloudName || !publicId) {
      return "";
    }

    const normalizedPublicId = decodeURIComponent(publicId)
      .split("/")
      .map((segment) => encodeURIComponent(segment))
      .join("/");

    return `https://res.cloudinary.com/${encodeURIComponent(cloudName)}/video/upload/f_auto,q_auto:good,so_0/${normalizedPublicId}.jpg`;
  } catch (_error) {
    return "";
  }
}

function getCloudinaryEmbedSrc(src, autoplay) {
  if (isMegaShareVideoSrc(src)) {
    return src.replace(/\/file\//i, "/embed/file/");
  }

  if (!isCloudinaryEmbedVideoSrc(src)) {
    return src;
  }

  try {
    const url = new URL(src);
    url.searchParams.set("autoplay", autoplay ? "true" : "false");
    url.searchParams.set("muted", "true");
    url.searchParams.set("loop", "true");
    url.searchParams.set("controls", "false");
    url.searchParams.set("playsinline", "true");
    return url.toString();
  } catch (_error) {
    return src;
  }
}

function getVideoPreviewTime(video) {
  const fallback = 0.12;
  if (!Number.isFinite(video.duration) || video.duration <= 0) {
    return fallback;
  }
  return Math.min(fallback, Math.max(0, video.duration - 0.02));
}

