let currentIndex = 0;
let autoplayTimer = null;
let isAnimating = false;
let lightboxOverlay = null;
let lightboxImage = null;
let lightboxVideo = null;
let lightboxEmbed = null;

function stopAutoplay() {
  if (!autoplayTimer) return;
  clearInterval(autoplayTimer);
  autoplayTimer = null;
}

function ensureLightbox() {
  if (lightboxOverlay) return;

  const overlay = document.createElement("div");
  overlay.className = "image-lightbox";
  overlay.setAttribute("aria-hidden", "true");

  const closeBtn = document.createElement("button");
  closeBtn.className = "image-lightbox-close";
  closeBtn.type = "button";
  closeBtn.setAttribute("aria-label", "Fermer l'image");
  closeBtn.textContent = "×";

  const image = document.createElement("img");
  image.className = "image-lightbox-img";
  image.alt = "Agrandissement photo";

  const video = document.createElement("video");
  video.className = "image-lightbox-video";
  video.controls = true;
  video.preload = "metadata";
  video.setAttribute("playsinline", "");

  const embed = document.createElement("iframe");
  embed.className = "image-lightbox-embed";
  embed.setAttribute("allow", "autoplay; fullscreen; picture-in-picture");
  embed.setAttribute("allowfullscreen", "");
  embed.setAttribute("loading", "lazy");
  embed.setAttribute("title", "Lecteur video");

  overlay.appendChild(closeBtn);
  overlay.appendChild(image);
  overlay.appendChild(video);
  overlay.appendChild(embed);
  document.body.appendChild(overlay);

  const closeLightbox = () => {
    overlay.classList.remove("open");
    overlay.setAttribute("aria-hidden", "true");
    document.body.classList.remove("lightbox-open");
    video.pause();
    video.removeAttribute("src");
    video.load();
    embed.removeAttribute("src");
    startAutoplay();
  };

  closeBtn.addEventListener("click", (event) => {
    event.preventDefault();
    event.stopPropagation();
    closeLightbox();
  });
  overlay.addEventListener("click", (event) => {
    if (event.target === overlay) {
      closeLightbox();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && overlay.classList.contains("open")) {
      closeLightbox();
    }
  });

  lightboxOverlay = overlay;
  lightboxImage = image;
  lightboxVideo = video;
  lightboxEmbed = embed;
}

function openLightbox(slide) {
  ensureLightbox();
  stopAutoplay();

  if (slide.mediaType === "video") {
    const playbackSrc = getCloudinaryDirectVideoSrc(slide.src, "lightbox");

    lightboxImage.style.display = "none";
    lightboxEmbed.style.display = "none";
    lightboxEmbed.removeAttribute("src");
    lightboxVideo.style.display = "block";
    lightboxVideo.onerror = null;
    lightboxVideo.src = encodeURI(playbackSrc);

    lightboxVideo.onerror = () => {
      if (!isEmbedVideoSrc(slide.src)) return;
      lightboxVideo.pause();
      lightboxVideo.style.display = "none";
      lightboxVideo.removeAttribute("src");
      lightboxVideo.load();
      lightboxEmbed.style.display = "block";
      lightboxEmbed.src = encodeURI(getCloudinaryEmbedSrc(slide.src, true));
    };

    lightboxVideo.play().catch(() => {
      // Browser policy can block autoplay with sound.
    });
  } else {
    lightboxVideo.pause();
    lightboxVideo.style.display = "none";
    lightboxVideo.removeAttribute("src");
    lightboxVideo.load();
    lightboxEmbed.style.display = "none";
    lightboxEmbed.removeAttribute("src");
    lightboxImage.style.display = "block";
    lightboxImage.src = encodeURI(slide.src);
    lightboxImage.alt = slide.title;
  }

  lightboxOverlay.classList.add("open");
  lightboxOverlay.setAttribute("aria-hidden", "false");
  document.body.classList.add("lightbox-open");
}

function createSlide(slide, index) {
  const item = document.createElement("article");
  item.className = "coverflow-item";
  item.dataset.index = index;

  const cover = document.createElement("div");
  cover.className = "cover";

  const reflection = document.createElement("div");
  reflection.className = "reflection";

  if (slide.mediaType === "video") {
    const playbackSrc = getCloudinaryDirectVideoSrc(slide.src, "card");
    const posterSrc = getCloudinaryPosterSrc(slide.src);

    const video = document.createElement("video");
    video.dataset.src = encodeURI(playbackSrc);
    video.muted = true;
    video.defaultMuted = true;
    video.loop = true;
    video.playsInline = true;
    video.preload = "none";
    video.setAttribute("playsinline", "");
    video.setAttribute("webkit-playsinline", "true");
    if (posterSrc) {
      video.poster = posterSrc;
    }
    cover.appendChild(video);

    const reflectionVideo = document.createElement("video");
    reflectionVideo.dataset.src = encodeURI(playbackSrc);
    reflectionVideo.muted = true;
    reflectionVideo.defaultMuted = true;
    reflectionVideo.loop = true;
    reflectionVideo.playsInline = true;
    reflectionVideo.preload = "none";
    reflectionVideo.setAttribute("playsinline", "");
    reflectionVideo.setAttribute("webkit-playsinline", "true");
    if (posterSrc) {
      reflectionVideo.poster = posterSrc;
    }
    reflection.appendChild(reflectionVideo);
  } else {
    const image = document.createElement("img");
    image.src = encodeURI(slide.src);
    image.alt = slide.title;
    image.loading = "lazy";
    cover.appendChild(image);
    reflection.style.backgroundImage = `url(${encodeURI(slide.src)})`;
  }

  item.appendChild(cover);
  item.appendChild(reflection);

  item.addEventListener("click", () => {
    if (index !== currentIndex) {
      currentIndex = index;
      update();
    }

    stopAutoplay();
    openLightbox(slides[index]);
  });

  return item;
}

function createDots() {
  slides.forEach((_, index) => {
    const dot = document.createElement("button");
    dot.className = "dot";
    dot.type = "button";
    dot.setAttribute("aria-label", `Aller à l'image ${index + 1}`);
    dot.addEventListener("click", () => {
      goTo(index);
    });
    dotsContainer.appendChild(dot);
  });
}

function build() {
  slides.forEach((slide, index) => {
    coverflow.appendChild(createSlide(slide, index));
  });
  createDots();
  update();
}

function update() {
  if (isAnimating) return;
  isAnimating = true;

  const items = [...document.querySelectorAll(".coverflow-item")];
  const dots = [...document.querySelectorAll(".dot")];

  items.forEach((item, index) => {
    let offset = index - currentIndex;
    const half = slides.length / 2;

    if (offset > half) offset -= slides.length;
    if (offset < -half) offset += slides.length;

    const abs = Math.abs(offset);
    const direction = Math.sign(offset);

    let x = offset * 190;
    let z = -abs * 195;
    let rotateY = -direction * Math.min(abs * 62, 62);
    let scale = 1 - abs * 0.13;
    let opacity = 1 - abs * 0.2;

    if (abs > 3) {
      opacity = 0;
      x = direction * 1200;
    }

    item.style.transform = `translate(-50%, -50%) translateX(${x}px) translateZ(${z}px) rotateY(${rotateY}deg) scale(${scale})`;
    item.style.opacity = opacity;
    item.style.zIndex = String(120 - abs);

    const reflection = item.querySelector(".reflection");
    if (reflection) {
      const reflectionOpacity = Math.max(0.34, 0.8 - abs * 0.12);
      reflection.style.opacity = String(reflectionOpacity);
    }

    const video = item.querySelector(".cover video");
    const reflectionVideo = item.querySelector(".reflection video");
    if (video) {
      const shouldKeepLoaded = abs <= 3 && opacity > 0.15;
      const shouldPlay = abs === 0 && opacity > 0.95;

      if (shouldKeepLoaded) {
        if (!video.src && video.dataset.src) {
          video.src = video.dataset.src;
          video.load();
        }
        if (reflectionVideo && !reflectionVideo.src && reflectionVideo.dataset.src) {
          reflectionVideo.src = reflectionVideo.dataset.src;
          reflectionVideo.load();
        }
      } else {
        video.pause();
        if (video.src) {
          video.removeAttribute("src");
          video.load();
        }
        if (reflectionVideo) {
          reflectionVideo.pause();
          if (reflectionVideo.src) {
            reflectionVideo.removeAttribute("src");
            reflectionVideo.load();
          }
        }
      }

      if (shouldPlay && video.src) {
        video.play().catch(() => {
          // Autoplay may fail silently on some browsers.
        });
        if (reflectionVideo && reflectionVideo.src) {
          reflectionVideo.currentTime = video.currentTime;
          reflectionVideo.play().catch(() => {
            // Autoplay may fail silently on some browsers.
          });
        }
      } else {
        video.pause();
        if (reflectionVideo) {
          reflectionVideo.pause();
        }
      }
    }

  });

  dots.forEach((dot, index) => {
    dot.classList.toggle("active", index === currentIndex);
  });

  titleEl.textContent = slides[currentIndex].title;
  descriptionEl.textContent =
    slides[currentIndex].mediaType === "video" ? "Voir video" : slides[currentIndex].description;

  setTimeout(() => {
    isAnimating = false;
  }, 660);
}

function goTo(index) {
  if (isAnimating || index === currentIndex) return;
  currentIndex = index;
  update();
}

function navigate(direction) {
  if (isAnimating) return;
  currentIndex = (currentIndex + direction + slides.length) % slides.length;
  update();
}

function startAutoplay() {
  if (autoplayTimer) return;
  autoplayTimer = setInterval(() => {
    navigate(1);
  }, 3800);
}

let touchStartX = 0;
let touchStartY = 0;

container.addEventListener(
  "touchstart",
  (event) => {
    touchStartX = event.changedTouches[0].screenX;
    touchStartY = event.changedTouches[0].screenY;
  },
  { passive: true }
);

container.addEventListener(
  "touchend",
  (event) => {
    const endX = event.changedTouches[0].screenX;
    const endY = event.changedTouches[0].screenY;

    const diffX = touchStartX - endX;
    const diffY = touchStartY - endY;

    if (Math.abs(diffX) > Math.abs(diffY) && Math.abs(diffX) > 40) {
      if (diffX > 0) navigate(1);
      else navigate(-1);
    }
  },
  { passive: true }
);

container.addEventListener("keydown", (event) => {
  if (event.key === "ArrowLeft") {
    navigate(-1);
  }
  if (event.key === "ArrowRight") {
    navigate(1);
  }
});

prevBtn.addEventListener("click", () => {
  navigate(-1);
});

nextBtn.addEventListener("click", () => {
  navigate(1);
});

if (backLink) {
  backLink.addEventListener("click", (event) => {
    event.preventDefault();
    const params = new URLSearchParams(window.location.search);
    const fromSection = params.get("fromSection");
    const targetSection =
      fromSection === "videography" ? "videography" : "photography";
    window.location.href = `index.html?section=${targetSection}&fromProject=1`;
  });
}

build();
startAutoplay();
